#include<stdio.h>
int main() {
	int a = 10, b = 3;
	printf("a = %d\n",a);
	printf("b = %d\n",b);
	printf("a = %d b = %d\n",a,b);
	printf("a + b = %d\n", a + b);
	printf("a - b = %d\n", a - b);
	printf("a * b = %d\n", a * b);
	printf("a / b = %f\n", a / b);
	printf("a %% b = %d\n", a % b);
}
