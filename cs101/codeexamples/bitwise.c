/*CS101C: This code example is used to demonstrate the use of bitwise operators 
 *author: Nikhil Hegde */

#include<stdio.h>
int main(){
	int a=10, b=3;
	printf("a & b: %d\n", a & b);
	printf("a | b: %d\n", a | b);
	printf("a ^ b: %d\n", a ^ b);
	printf("~a: %d\n", ~a);
	printf("a << 1: %d\n", a << 1);
	printf("a >> 1: %d\n", a >> 1);

}
